package echo

import (
	"net/http"
	"prc_hub_back/application/event"
	"prc_hub_back/domain/model/jwt"

	"github.com/labstack/echo/v4"
)

// (GET /events/{id}/documents)
func (s Server) GetEventsIdDocuments(ctx echo.Context, id Id, params GetEventsIdDocumentsParams) error {
	// Get jwt claim
	jcc, err := jwt.CheckProvided(ctx)
	if err != nil {
		return JSONMessage(ctx, http.StatusUnauthorized, err.Error())
	}

	// Get documents
	documents, err := event.GetDocumentList(event.GetDocumentQueryParam{EventId: &id}, jcc.Id)
	if err != nil {
		return JSONMessage(ctx, event.ErrToCode(err), err.Error())
	}

	return JSONPretty(ctx, http.StatusOK, documents)
}
